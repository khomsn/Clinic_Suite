<font size="4"><b>Routine Adult Physical Examination --Check List--</b></font>
<p>
<table width="80%" border="1" align="center">
 <tbody>
 <tr><td>General Inspection/<br>Upper Extremities</td>
     <td>2. Inspect both hands (nails, dorsum, palms).<br>
         3. Inspect and palpate arms.<br>
         4. Palpate epitrochear nodes.<br>
         5. Test muscle strength in hand grip, and flex joints (wrists and elbows).</td></tr>
 <tr><td>Vital Signs</td>
     <td>6. Pulse in radial artery for 10 seconds.<br>
         7. Compare both radial pulses.<br>
         8. Blood pressure in right arm.</td></tr>
 <tr><td>Head</td>
     <td>9. Inspect scalp and hair.</td></tr>
 <tr><td>Eyes</td>
     <td>10. Measure visual acuity (each eye separately and together).<br>
         11. Inspect external eye (conjunctive, eyelids, sclera).<br>
         12. Extraocular movements (6 positions).<br>
         13. Pupil reaction to light (direct, consensual).<br>
         14. Visual fields by confrontation (each eye separately).<br>
         15. Ophthalmoscopy exam (dims lights, positions patient appropriately).<br>
         16. Holds ophthalmoscope with correct hand (i.e. right for right eye) and index finger to switch lenses.<br>
         17. Verbalize structure examined.</td></tr>
 <tr><td>Ears</td>
     <td>18. Hearing test using wrist watch or whispered number (if abnormal, tuning fork).<br>
         19. Inspect external ear.<br>
         20. Pulls on pinna (auricle), push on tragus.<br>
         21. Otoscope exam bilaterally.</td></tr>
 <tr><td>Nose/Mouth</td>
     <td>22. Inspects nasal septum and turbinates with otoscope or speculum.<br>
         23. Palpate frontal and maxillary sinuses.<br>
         24. Inspect mouth with blade: tongue, mucosa, ducts, tonsillar fossa, pharynx.<br>
         25. Phonate and protrude tongue; observe palate and uvula.</td></tr>
 <tr><td>Neck/Shoulder/Back</td>
     <td>26. Palpate lymph nodes (verbalize node groups).<br>
         27. Palpate thyroid.<br>
         28. Palpate trachea.<br>
         29. Neck range of motion, to sides, forward, back.<br>
         30. Shoulder shrug - test strength.<br> 
         31. Palpate spine.<br>
         32. Fist percussion of costovertebral angles.</td></tr>
 <tr><td>Chest</td>
     <td>33. Inspect skin.<br>
         34. Check symmetry with deep breath.<br>
         35. Percuss posteriorly and laterally.<br>
         36. Measure diaphragm excursion.<br>
         37. Instruct patient to breathe throuth open mouth.<br>
         38. Auscult posterior and laterally.<br>
         39. Palpate axillary lymph nodes.<br>
         40. Inspect breast.<br>
         41. Percuss anterior lung fields.<br>
         42. Auscult anterior lung fields.<br>
         43. Auscult heart with diaphragm at left sternal border (3-4th ICS).</td></tr>
 <tr><td>Breasts</td>
     <td>44. Inspect and palpate.</td></tr>
 <tr><td>Cardiovascular</td>
     <td>45. Inspect neck veins.<br>
         46. Auscult carotids at upper and lower extents.<br>
         47. Palpate one carotid at a time.<br>
         48. Inspect and palpate precordium.<br>
         49. Auscult each valve area with diaphragm.<br>
         50. Auscult each valve area with bell.<br>
         51. Ask patient to roll in left lateral position; palpates apex.<br>
         52. Auscult with bell at apex.</td></tr>
 <tr><td>Abdomen</td>
     <td>53. Inspect.<br>
         54. Auscult epigastrium, 4 quadrants.<br>
         55. Palpate 4 quadrants (superficial and deep).<br>
         56. Palpate liver edge in deep inspiration.<br>
         57. Percuss liver dullness (measure span in midclavicular line).<br>
         58. Palpate spleen in deep inspiration.<br>
         59. Percuss for splenic dullness.<br>
         60. Palpate and measure aortic pulsation.<br>
         61. Palpate inguinal nodes.<br>
         62. Palpate and auscult femoral pulses. </td></tr>
 <tr><td>Lower Extremities</td>
     <td>63. Inspect and palpate legs.<br>
         64. Check range of motion, hips, knees.<br>
         65. Test for ankle edema.<br>
         66. Check dorsalis pedes and posterior tibial pulses.<br>
         67. Wrinkle forehead, show teeth, protrude tongue, eye closure (may be done during head exam).<br>
         68. Extend arms for drift.<br>
         69. Check muscle strength in upper and lower extremities (proximal and distal).<br>
         70. Reflexes - biceps, ankle, knee.<br>
         71. Plantar reflex bilaterally.<br>
         72. Test sensation to pinprick.<br>
         73. Test vibration (128 cps) and position sense in lower extremities.<br>
         74. Test finger to nose.<br>
         75. Test heel to shin.<br>
         76. Check gait.<br>
         77. Toe to heel walking.<br>
         78. Romberg test.</td></tr>
 <tr><td>Male Genitalia<br>(may be deferred)</td>
     <td>79. Inspect and palpate penis.<br>
         80. Palpate testes and scrotum.<br>
         81. Performs hernia exam.</td></tr>
 <tr><td>Rectal Exam</td>
     <td>82. Rectal exam.</td></tr>
