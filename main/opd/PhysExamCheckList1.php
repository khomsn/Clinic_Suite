<font size="4"><b>Physical Exam Check List</b></font>
<br>originated and provided by Dr. GH Stein.
<p>
This check list has been used to study the quantitative physical exam skills of entering first year residents
<br>at Kameda Medical Center, Kamogawa City.
</p><p>
Part of the results were presented by Dr. GH Stein
<br>under the title of "Physical Examination Skills of First Year Residents"
<br>at the 29th Annual Meeting of Japanese Association for Medical Education
<br>held on July 18, 1997 at Kanazawa City, Japan.
</p><p>
